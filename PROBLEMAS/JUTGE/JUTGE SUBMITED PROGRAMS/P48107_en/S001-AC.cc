#include <iostream>

int main() {
  int a, b;
  b > 0;
  std::cin >> a;
  std::cin >> b;

if (b > 0) {
  int d, r;
  d = a / b;
  r = a % b;
  std::cout << d << " " << r << std::endl;
}
else {
    return 0;
}
return 0;
}

